class Car:
    weight = 3000
    make = "Toyota"
    model = "Camry"
    price = 28000
    year = 2024

    def printCarInfo(self):
        print("Weight: ", self.weight)
        print("Make: ", self.make)
        print("Model: ", self.model)
        print("Price: ", self.price)
        print("Year: ", self.year)

    


toyota1 = Car()

toyota1.printCarInfo()


